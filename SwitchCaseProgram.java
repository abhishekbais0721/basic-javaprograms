package basicprogram;

public class SwitchCaseProgram {

	public static void main(String[] args) {
		
		switch(9)
		{
		case 1: System.out.println("Launch my Chrome Browser"); 
				break;
		case 2: System.out.println("Launch my safari Browser"); 
				break;
		case 3: System.out.println("Launch my firefox Browser"); 
				break; 
		case 4: System.out.println("Launch my opera mini Browser"); 
				break; 
				
				default: System.out.println("Sorry your selection is wrong");
		}


	}

}
